    private void btnVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyActionPerformed
             if (!isValidateOtp()) {
                     JOptionPane.showMessageDialog(null, "Invalid Otp");
                     return;
            }
        try {
                 AppointmentPojo  appt = new AppointmentPojo();
                appt.setPatientName(txtPatientName.getText().trim());
                appt.setMobileNo(txtPhoneNo.getText().trim());
                appt.setApptStatus(pat.getStatus());
                appt.setOpd(pat.getOpd());
                appt.setPatientId(pat.getPatientId());
                appt.setDateTime(String.valueOf(pat.getPdate()));
                String docName = DoctorsDao.getDoctorNameById(pat.getDoctorId());
                appt.setDoctorName(docName);
                boolean result2 = AppointmentDao.addPatient(appt);
                boolean result1 = PatientDao.addPatient(pat);
               
                if (result1 && result2) {
                    JOptionPane.showMessageDialog(null, "Patient Register SuccessFully");
                    ViewAllPatientsFrame viewFrame = new ViewAllPatientsFrame();
                    viewFrame.setVisible(true);
                    this.dispose();

                } else {
                    JOptionPane.showMessageDialog(null, "Sorry! ,Patient  Registeration UnsuccessFully", "Error", JOptionPane.ERROR_MESSAGE); 
                }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnVerifyActionPerformed

