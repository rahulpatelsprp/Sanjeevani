    private boolean isValidateOtp() {
        boolean result=false;
        try{
            otp=Integer.parseInt(txtOtp.getText().trim());
            if(pat.getOtp()==otp)
              result=true;
           
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"You Entered Non numeric Otp"+ex);
            ex.printStackTrace();
        }
      finally{
        return result;
        }
    }

     private boolean isContactNoValid() {
        mobNo=txtPhoneNo.getText().trim();
         char[] mob = mobNo.toCharArray();
        for (char ch : mob) {
            if (Character.isDigit(ch) == false) {
                return false;
            }
        }
        if (mobNo.length() == 10||!mobNo.isEmpty()) {
            pat.setMobileNo(mobNo);
            return true;
        }
        return false;
    }
   
