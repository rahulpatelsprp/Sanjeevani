    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        try {
            Sender sd = new OTPsender();
            if(!isContactNoValid()){
            JOptionPane.showMessageDialog(null,"Invalid Numbers");
            return;
            }
            boolean reply = sd.send(mobNo, String.valueOf(pat.getOtp()));
            if (reply) {
                JOptionPane.showMessageDialog(null, "OTP Sent Successfully");
            } else {
                JOptionPane.showMessageDialog(null, "Can't send otp Unsuccessfully,Please Check the Number");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error in Sending Otp:" + ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSendActionPerformed

