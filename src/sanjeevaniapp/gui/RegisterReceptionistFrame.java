    private void loadAllRecptionistDetails() {
        try {
            unRegRecpList = EmpDao.getUnRegisteredReceptionist();
            Set<String> empIdSet = unRegRecpList.keySet();
             for (String empId : empIdSet) {
                jcEmployeeId.addItem(empId);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex);
            ex.printStackTrace();
        }
    }

    private void genNewRecpId() {
        try {
            String docId = ReceptionistDao.getNewRecId();
            txtReceptionistId.setText(docId);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex);
            ex.printStackTrace();
        }
    }

    private boolean validateInputs() {
        this.logId = this.txtLoginId.getText().trim();
        char[] p1 = this.txtPwd1.getPassword();
        char[] p2 = this.txtPwd2.getPassword();

        if (logId.isEmpty() || p1.length == 0 || p2.length == 0) {
            return false;
        }
        pwd1 = new String(p1);
        pwd2 = new String(p2);
        return true;

    }

    private String matchPassword() {
        pwd1 = pwd1.trim();
        pwd2 = pwd2.trim();
        if (pwd1.length() < 3 || pwd2.length() < 3) {
            return "Passwords must be of atleast 3 characters in length";
        }
        if (pwd1.equals(pwd2) == false) {
            return "Passwords do not match";
        }
        return null;
    }

    private void clearAll() {
        txtLoginId.setText("");
         this.txtPwd1.setText("");
        this.txtPwd2.setText("");
        this.unRegRecpList.remove(selEmpId);
        this.jcEmployeeId.removeItem(selEmpId);
         if(unRegRecpList.size()==0){
          JOptionPane.showMessageDialog(null,"All Receptionist registered!");
          ManageReceptionistFrame manageRecep=new ManageReceptionistFrame();
          manageRecep.setVisible(true);
          this.dispose();
      }
    }
