    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (validateInputs() == false) {
            JOptionPane.showMessageDialog(null, "Please input all the fields");
            return;
        }
        String passwordValid = matchPassword();
        if (passwordValid != null) {
            JOptionPane.showMessageDialog(null, passwordValid);
            return;
        }
        try {
            ReceptionistPojo rec = new ReceptionistPojo();
            rec.setReceptionistId(txtReceptionistId.getText().trim());
            rec.setReceptionistName(selEmpName);
            rec.setGender(jcGender.getSelectedItem().toString());
            UserPojo user = new UserPojo();
            user.setLoginId(logId);
            String encPassword=PasswordEncryption.getEncryptedPassword(pwd1);
            user.setPassword(encPassword);
            user.setUserName(selEmpName);
            user.setUserType("RECEPTIONIST");
            boolean result1 = ReceptionistDao.addReceptionist(rec);
            boolean result2 = UserDao.addUser(user);
            if (result1 == true && result2 == true) {
                JOptionPane.showMessageDialog(null, "Receptionist successfully registered!");
                clearAll();
                this.genNewRecpId();
                return;
            }
            JOptionPane.showMessageDialog(null, "Registration unsuccessful!");
            return;

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex);
            ex.printStackTrace();
        }


    }//GEN-LAST:event_btnRegisterActionPerformed

