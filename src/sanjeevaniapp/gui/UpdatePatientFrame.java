    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (validateInputs() == false) {
            JOptionPane.showMessageDialog(null, "Please input all the fields");
            return;
        }
        if (isAgeValid() == false) {
            JOptionPane.showMessageDialog(null, "Invalid Age");
            return;
        }
        if (isContactNoValid() == false) {
            JOptionPane.showMessageDialog(null, "Invalid mobile no");
            return;
        }
        setDetailsInPojo();
        try {
            AppointmentPojo appt = new AppointmentPojo();
            appt.setPatientName(firstName + " " + lastName);
            appt.setPatientId(jcPatientId.getSelectedItem().toString());
            appt.setMobileNo(contactNo);
            appt.setOpd(opd);
            appt.setDateTime(String.valueOf(txtDate.getText().trim()));
            String docName = DoctorsDao.getDoctorNameById(jcDoctorId.getSelectedItem().toString());
            appt.setDoctorName(docName);
            boolean result2 = AppointmentDao.updatePatient(appt);
            boolean result1 = PatientDao.updatePatient(pat);
            System.out.println(result1);
            System.out.println(result2);
            if (result1 && result2) {
                JOptionPane.showMessageDialog(null, "Patient Updated SuccessFully");
                ViewAllPatientsFrame viewFrame = new ViewAllPatientsFrame();
                viewFrame.setVisible(true);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Sorry! ,Patient Not Updated", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

