 private void loadAllDoctorId() {
        try {
            List<String> docId = DoctorsDao.getAllDoctarId();
            if (docId.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Doctor's Not Register", "Error", JOptionPane.ERROR_MESSAGE);
                ReceptionistOptionsFrame recepFrame = new ReceptionistOptionsFrame();
                recepFrame.setVisible(true);
                this.dispose();
            }
            for (String id : docId) {
                jcDoctorId.addItem(id);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex);
            ex.printStackTrace();
        }
    }

    private void loadAllPatientId() {
        try {
            List<String> patId = PatientDao.getAllPatientId();
            if (patId.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Patients Record Not Found", "Error", JOptionPane.ERROR_MESSAGE);
                ReceptionistOptionsFrame recepFrame = new ReceptionistOptionsFrame();
                recepFrame.setVisible(true);
                this.dispose();
            }
            for (String id : patId) {
                jcPatientId.addItem(id);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex);
            ex.printStackTrace();
        }
    }

    private void loadAllPatientDetails() {
        txtOpd.setText(patDetails.getOpd());
        txtFirstName.setText(patDetails.getFirstName());
        txtLastName.setText(patDetails.getLastName());
        txtAge.setText(String.valueOf(patDetails.getAge()));
        txtCity.setText(patDetails.getCity());
        txtAddress.setText(patDetails.getAddress());
        txtPhoneNo.setText(patDetails.getMobileNo());
        txtDate.setText(String.valueOf(patDetails.getPdate()));

    }

    private boolean validateInputs() {
        age = txtAge.getText().trim();
        this.contactNo = this.txtPhoneNo.getText().trim();
        this.address = this.txtAddress.getText().trim();
        this.firstName = this.txtFirstName.getText().trim();
        this.lastName = this.txtLastName.getText().trim();
        this.opd = this.txtOpd.getText().trim();
        this.city = this.txtCity.getText().trim();

        if (age.isEmpty() || address.isEmpty() || firstName.isEmpty() || lastName.isEmpty() || opd.isEmpty() || city.isEmpty()) {
            return false;
        }

        return true;

    }

    private boolean isContactNoValid() {
        char[] mob = contactNo.toCharArray();
        for (char ch : mob) {
            if (Character.isDigit(ch) == false) {
                return false;
            }
        }
        if (contactNo.length() == 10) {
            return true;
        }
        return false;
    }

    private boolean isAgeValid() {
        char[] age1 = age.toCharArray();
        for (char ch : age1) {
            if (Character.isDigit(ch) == false) {
                return false;
            }
        }

        return true;
    }

    private void setDetailsInPojo() {
        pat = new PatientPojo();
        pat.setPatientId(jcPatientId.getSelectedItem().toString());
        pat.setAddress(address);
        pat.setAge(Integer.parseInt(age));
        pat.setDoctorId(jcDoctorId.getSelectedItem().toString());
        pat.setCity(city);
        pat.setFirstName(firstName);
        pat.setLastName(lastName);
        pat.setGender(jcGender.getSelectedItem().toString());
        pat.setMarriedStatus(jcMarriedStatus.getSelectedItem().toString());
        pat.setMobileNo(contactNo);
        pat.setOpd(opd);

    }
